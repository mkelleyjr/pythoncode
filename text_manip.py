# text manipulation in python - some examples

# start by defining a string 
my_string = "Welcome"
print(my_string)

# make it uppercase 
upper = my_string.upper()
print(upper)

# make it lowercase 
lower = my_string.lower()
print(lower)

# find the length of the string 
length = len(my_string)
print(length)
